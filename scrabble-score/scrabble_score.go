package scrabble

import (
	"strings"
)

// Score returns the scrabble score for input string.
func Score(input string) (score int) {
	input = strings.ToUpper(input)

	for _, ch := range input {
		switch ch {
		case 'A', 'E', 'I', 'O', 'U', 'L', 'N', 'R', 'S', 'T':
			score++
		case 'D', 'G':
			score += 2
		case 'B', 'C', 'M', 'P':
			score += 3
		case 'F', 'H', 'V', 'W', 'Y':
			score += 4
		case 'K':
			score += 5
		case 'J', 'X':
			score += 8
		case 'Q', 'Z':
			score += 10
		}
	}

	return score
}
